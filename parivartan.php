      <div id="soch" class="section scrollspy">
        <h5>Soch</h5>
		<blockquote><b>“ Think | Believe | Change ”</b></blockquote>
        <p>Soch aims at addressing various contemporary social issues and arriving at practical solutions to these issues. Our motto is - “Think, Becaue you can change”. We primely focus on the issues that got deep rooted in the society, but are subjected to disregard. Soch is also the mainstream social initiative of VARCHAS, the annual Sports Festival of IIT Jodhpur. Which means using a platform where young spirits meet as a stage of awareness on burning issues.</p>
        <h6><b>Activities</b></h6>
        <p>
			<li>
				<dt>Surveys and social experiments</dt>
				<dd>-- Every year, we focus on one problem, and we try to find out how people actually look at it. Talks and opinion polls at public places and gatherings is one way to do that.</dd>
			</li>
			<li>
				<dt>Awareness drives with school children</dt>
				<dd>-- We stress on our future citizens, they should be aware about societal issues and a proper prospective. So we spend time with them.</dd>
			</li>
			<li>
				<dt>Discussions and debates</dt>
				<dd>-- ‘Awaaz’ is our very reputed platform where discussions and brain storming by profound personalities take place annually during ‘Varchas’. We chose many other ways  to provide a food for thought. Nukkad/Drama are a very good medium to tell your tale.</dd>
			</li>
		</p>
