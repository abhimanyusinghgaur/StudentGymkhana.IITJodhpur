        <h5>Health & Hygiene Wing</h5>
        <blockquote><b>“ Good health and good sense are two of life’s greatest blessings. ” - Publilius Syrus</b></blockquote>
		<p>The main aim of this department is to make people aware of their health and fitness and to maintain hygiene level in the society.</p>
        <h6><b>Activities</b></h6>
        <p>
			<li>A health camp was organized at Sangeria, by providing free medical checkups, medicines and thus catering to health improvement over 176 villagers.</li>
			<li>Knowing the importance of blood, the greatest gift to mankind, a blood donation camp was organized at GPRA (our residential area) which received a huge response from people all over Jodhpur.</li>
			<li>With an Aim of eradicating polio among the children of Sangeria Village, polio drops were given to them.</li>
			<li>Career Counselling was done in a government school near GPRA to support the emotional health and well-being of these young people and help them build a healthy future.</li>
			<li>Celebration of 'Global Hand Wash Day' on 15th October.</li>
		</p>
