        <h4 class="center">Parivartan</h4>
        <blockquote><b>“ I slept and dreamt that life was joy. I woke up and saw, life was service, I acted and behold service was joy. ” – Rabindranath Tagore.</b></blockquote>
		<p>Exposure! Exposure is everything. One lives in a geographically better location, amidst of good nourishment, schooling opportunities and employment sooner or later follows. Nothing routine, could probably stop him from being the best of what he desires. Now, the lack of exposure and opportunities? Yes, the second side do troubles a lot!</p>
		<p>We here at IITJ, joined our hands together for a dream.  To bring a change, a ‘Parivartan’ and bridge the gap by at least one step forward, towards equality. Because no one is cursed to live a deprived life. It’s just lack of exposure. Exposure to their opportunities, rights and facilities they could have. Darkness is nothing but shadow created by blocking light. And many parts of our society had been living in the darkness for too long that they need an aid to search their way out.</p>
		<p>To create a good sense of social responsibility among IITJ community and come together for a cause that spreads a positive vibrance around us. To lend a helping hand to the needy and build a healthy, prosperous society is the motive that drives Parivartan. At the same time a better knowledge of the society and its feedback loops with a hands on experience is gained by students.</P>
