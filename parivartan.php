      <div id="environmentalAwareness" class="section scrollspy">
        <h5>Environmental Awareness Wing</h5>
		<blockquote><b>“ What if we create a world where environment doesn’t need to be ‘protected’? ”</b></blockquote>
        <p>Environment is one of the major factors that concerns the wellbeing of a society. It provides us various resources and saves us from many dangers. It provides conditions for development and growth. The environment is being damaged which in turn harms us. It is our duty to safeguard it. So as much awareness as possible should be spread among various people to protect it. </p>
        <h6><b>Activities</b></h6>
        <p>
			<li>We conduct various competitions in schools and educational institutions and make children aware and also invoke their interest in protecting the environment. </li>
			<li>Perform nukkad (street play) and other awareness activities in villages to educate them about this issue.</li>
			<li>Conduct drives in the streets to make the shopkeepers and other people change their perspective into doing their best in keeping the surroundings clean.</li>
		</p>   
