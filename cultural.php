        <h5>Drama Club</h5>
        <p>The Dramatics Club, by the name of DRAMEBAAZ, not only attracts widespread participation from the students, but is also loved by the audience. The club primarily focuses on Street Plays which have always been its forte. High on energy and with a definitive outlook, the club members manage to leave the people enthralled with their performances. The club picks up on the issues faced by the society as theme of their street play. When this amalgamates with brilliant scripting and tongue-in-cheek humour, fireworks are bound to happen.A highly demanding and highly challenging club in its own right, it provides a great sense of satisfaction to those who have an inclination towards performance arts.</p>
