      <div id="Vision" class="section scrollspy">
        <h4 class="center">Cultural Society</h4>
        <p>Cultural Society is the fountainhead of all cultural activities in the Institute. Cultural activities prepare you for real life and strengthen your personal skills. They not only help students to identify themselves with the Institute, but also assist students to develop themselves in a desired field and to improve skills such as organization, presentation, leadership and interpersonal communication. Under the Cultural Society there are five clubs, which function throughout the year.</p>        
      </div>
      <div id="Dance" class="section scrollspy">
        <h5>Dance Club</h5>
        <p>Whether you are a master or an eager learner, the Dance Club is open for all. The club members come and practice regularly to hone their skills. Called ‘DEFEAT THE BEAT’, the club performs in various festivals and events occurring across the country including Chaos (IIM Ahmedabad) and Mood Indigo (IIT Bombay). The club also organizes workshops on different dance styles .Various intercollegiate events are also conducted .The team added a feather to its cap by getting selected in Indian Hip Hop Championship auditions in 2014.</p>
