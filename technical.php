      <div id="Programming" class="section scrollspy">
        <h5>Programming Club</h5>
        <p>We are the programming fraternity of IIT Jodhpur. Established the year our college started, we have gone from strength to strength in the past years. Whether it is algorithmic programming challenges, AI games, hacking on something cool, we do it together. So if programming turns you on, come on and get wired in!</p>
        <p>We at Programming Club do not believe that a person is limited to just one field. Hence we have these three cells which promote and help to develop skills in their respective fields:-
			<li>
				<dt>"Donalds" - Data structures and Algorithms</dt>
				<dd>-- Competitive coding is the main agenda of meetups in this cell. Members of this cell know where to find all resources to enhance one’s competitive coding skills, how and where to practice, what to do in order to improve oneself at every stage in the vast field of algorithms.</dd>
			</li>
			<li>
				<dt>"Assemblers" - Software, Web, and Mobile Development</dt>
				<dd>-- This cell consists of the best developers in the club. Be it software, web tech, mobile apps, or anything new, fancy and helpful to the world, this cell accommodates for all the fields.</dd>
			</li>
			<li>
				<dt>"Carpe Diem" - Cyber and Software Security (Hacking)</dt>
				<dd>-- This cell consists of meticulous programmers who love freedom (hackers). From script kiddies like phishing, unauthorized code injections, email-spoofing and cookie hacks to bolder and conceptual topics like cryptography, binary exploitation, reverse code engineering, steganography and network packet sniffing, we have it all.</dd>
			</li>
		</p>
		<h6><b>Activities</b></h6>
        <p>
			<li>Participate in almost all major competitive coding contests including ACM-ICPC.</li>
			<li>Organize DHPCs (Dark Hour Programming Contests) for regular practice.</li>
			<li>Contribute to internationally well known open source software like Mozilla Firefox, DuckDuckGo, some well known Python libraries and many more.</li>
			<li>Participate in Google Summer of Code (GSoC).</li>
			<li>Build cool apps and host them on Android playstore and/or Windows appstore and/or on the web.</li>
			<li>Participate in national and international Hacking competitions including Microsoft’s annual hacking championship, InCTF, and other international events on CTFtime.</li>
			<li>Conduct workshops and lectures regarding algorithms and data structures, hacking and latest web & mobile technologies, as required and requested by the students.</li>
		</p>
        <h6><b>Achievements</b></h6>
        <p>
			<li>Every year many teams qualify for ACM ICPC regionals.</li>
			<li>Last year a team got 29th ranks at an ACM ICPC regional.</li>
			<li>We have our own, fully-functional code-judge on which we can host competitive coding challenges, made by club members.</li>
			<li>Many selections in Google Summer of Code.</li>
		</p>   
