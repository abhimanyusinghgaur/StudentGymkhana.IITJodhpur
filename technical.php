      <div id="Automobile" class="section scrollspy">
        <h5>Automobile Club</h5>
        <p>Automobile club IIT Jodhpur is a group of passion-driven students who strive to constantly design and build cars and vehicles of all types. Ideas, thoughts, questions, basically anything and everything that screams "automotive", even plain feelings of infatuation are what drives the members of this club. Their vision is to promote and encourage interested and passionate students to design and build their own car. </p>
        <h6><b>Activities</b></h6>
        <p>In order to quench their thirst of exploring the automobile world, the automobile club is constantly busy organizing lectures, workshops, quizzes and similar discussion sessions. Lectures are given on the fundamental systems of automobiles, CAD design softwares like Solidworks and Ansys and topics like Vehicle Dynamics. In order to explore the practical aspect of things workshops are conducted such as Engine dismantling, Vehicle dynamics and short workshops for mini projects like solar car and IC car. Along with this the club has shown its presence in many all India competitions and has ranked well among the tough competition like SAE-Efficycle competition and SAE- BAJA All-Terrain Vehicle competition. Small workshops are also conducted such as Solaris, where students of the college are encouraged to design and build a small solar car and race it through a track course in order to test the vehicles structural stability and nimbleness. These events are also conducted in the colleges fests such as the Intra college technical fest Nimble and inter college Techno-cultural fest Ignus.</p>
