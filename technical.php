        <h5>Robotics Club</h5>
        <p>Everyone should learn robotics and why should you learn it or why would anyone would want to learn it? Remember when you were kid you took stuff apart to see how it works and some time for repairing your favorite toy. Why do it? Because ROBTICS is ultimate blank canvas. It is human right to be able to dig into anything you have in front of you from phone in your hand to earphones in your ear to those remote controlled cars you had played with. You want to change the way anything works. You want your car should go around building autonomously or may be controlled by your voice. So that’s why we do it because it’s becoming more near to ourselves, an expression, and an extension of our intelligent of our mentality to create and to destroy in some ways. That’s why we do it. Point of club is to help you to get more into the tools necessary to those things, to help you to create and to augment your possessions and to destroy and recreate and that’s what we are all about at ROBOTICS CLUB.</p>
