        <h5>Electronis Club</h5>
        <p>Electronics club, IIT Jodhpur is a group of highly enthusiastic and motivated individuals who aim to promote and enhance the knowledge of all the IITJ fraternity into the vast and seemingly endless world of electronic gadgets.</p>
		<p>We aim to introduce the plethora of concepts in a simple, interactive and understandable way. We conduct a variety of events ranging from the simplest of logic related problems to the most complex ones including serial communication, microcontrollers, simulation tools etc.</p>
        <h6><b>Activities</b></h6>
        <p>Many interesting events have been conducted by our club. These include flagship events like:-</p>
			<li>Technovation wherein students designed a smart traffic light signal system using multiplexors, flip-flops and basic logic gates.</li>
			<li>Minicomputer using Arduino and keyboard to compute basic mathematics and display it on screen.</li>
			<li>Even workshops are organized on various topics like PCB design and development etc.</li>
		<p>What next? – The club is highly motivated to follow topics like FPGA (Field Programmable Gate Array), Hardware description languages, simulation and communication based events, strobe light and much more to follow. We are on the lookout for interested hobby enthusiasts to take the club to newer horizons.</p>
        <h6><b>Achievements</b></h6>
        <p>
			<li>Team representing IIT Jodhpur got selected and reached the quarterfinals for Texas Instruments competition based on smart traffic light design. The aim was to sense the bulk of the traffic and trigger signals accordingly.</li>
			<li>We also had a summer project on propeller clock wherein students displayed time using microcontroller and dc motors. The main idea was to create persistence of vision among viewers and hence display the time.</li>
